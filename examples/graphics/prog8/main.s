.syntax asmpsx

VS_IO equ 0x1F800000
VS_GP0 equ 0x1810
VS_GP1 equ 0x1814
VS_FILL_SCREEN equ 0x2000000
VS_CPU_TO_VRAM equ 0xA0000000
VS_TEXTURE_FOUR_POINT_POLY equ 0x2D000000
VS_GPU_DMA equ 0x10A0
VS_GPU_BCR equ 0x10A4
VS_GPU_CHCR equ 0x10A8
VS_MODE equ 0 
VS_WIDTH equ 256 
VS_HEIGHT equ 240

VS_IMGW equ 100 
VS_IMGH equ 65

; CHOOSE R,G, B VALUES FROM 0 TO 255

VS_R equ 0
VS_G equ 0
VS_B equ 0

; vs_gp0 = vs_io_addr + vs_gp0;
; vs_gp1 = vs_io_addr + vs_gp1;

InitVideo:
	li t0, VS_IO           ; vs_io_addr = 0x1F800000; (base i/o address of memory map) 
	sw zero, VS_GP1(t0)   ; *vs_gp1 = vs_cmd_gpu_reset;
	li t1, 0x4000000       ; vs_cmd_dma_req = 0x4000000 (no dma)
	sw t1, VS_GP1(t0)      ; *vs_gp0 = vs_cmd_dma_req;
	li t1, 0x8000000       ; vs_cmd_display_mode = 0x8000000 + mode;
	addiu t1, VS_MODE ; vs_cmd_display_enable += mode;
	sw t1, VS_GP1(t0)     ; *vs_gp1 = vs_cmd_display_enable;
	li t5, 0xC4E24E        ; vs_hrange = 0xC4E24E;
	li t6, 0x040010        ; vs_vrange = 0x040010;
	li t1, 0x06000000      ; vs_cmd_horizontal_range = 0x6000000
	addu t1, t5      ; vs_cmd_horizontal_range += vs_hrange;
	sw t1, VS_GP1(t0)     ; *vs_gp1 = vs_cmd_horizontal_range;
	li t1, 0x07000000      ; vs_cmd_vertical_range = 0x7000000;
	addu t1, t6      ; vs_cmd_vertical_range += vs_vrange;
	sw t1, VS_GP1(t0)     ; *vs_gp1 = vs_cmd_vertical_range;
	li t1, 0x05000000      ; vs_cmd_display_x1y1 = 0x5000000; (start x, y of display = 0)
	sw t1, VS_GP1(t0)     ; *vs_gp1 = vs_cmd_display_x1y1;
	li   t1, 0xE1000000    ; cmd = gpu0_cmd_tex_page;
	addi t1, t1, 0x000508 ; cmd += 0x000508;  
	sw   t1, VS_GP0(t0)   ; *vs_gpu0 = cmd;
	li   t1, 0xE3000000    ; cmd = gpu0_cmd_draw_area_top_left;
	sw   t1, VS_GP0(t0)   ; *vs_gpu0 = cmd;
	li   t1, 0xE4000000    ; cmd = gpu0_cmd_draw_area_bot_right;
	li   t2, VS_HEIGHT
	li   t3, VS_WIDTH
	sll  t2, t2, 0x0A     ; height <<= 10;
	addu t2, t3      ; height += width;
	addu t1, t2      ; cmd += height;
	sw   t1, VS_GP0(t0)   ; *vs_gpu0 = cmd;
	li   t1, 0xE5000000    ; cmd = gpu0_cmd_draw_offset;
	sw   t1, VS_GP0(t0)   ; *vs_gpu0 = cmd;
	li   t1, 0x03000000    ; cmd = gpu1_cmd_display_enable;
	sw   t1, VS_GP1(t0)   ; *vs_gpu1 = cmd;
	li t2, 0x1F8010F0      ; dma_address = 0x1F8010F0;
	li t1, 0x300           ; dma_priority = 0x300;
	sw t1,(t2)          ; *dma_address = dma_priority;
	li t1, 0x800           ; gpu_dma_enable = 0x800;
	sw t1,(t2)          ; *dma_address = gpu_dma_enable;
FillScreen:
	li t1, VS_FILL_SCREEN  ; vs_cmd_fill_screen = VS_FILL_SCREEN;
	li t2, VS_B 
	andi t2, 0xff     ; b &= 0xff;
	sll t2, 0x10      ; b <<= 16;
	li t3, VS_G 
	andi t3, 0xff     ; g &= 0xff;
	sll t3, 0x8       ; g <<= 8;
	addu t2, t3      ; b += g;
	addiu t2, VS_R    ; b += r;
	addu t1, t2      ; vs_cmd_fill_screen += b;
	sw t1, VS_GP0(t0)     ; *vs_gp0 = vs_cmd_fill_screen;
	sw zero, VS_GP0(t0)   ; *vs_gp0 = 0; (x1, y1 = 0) 
	li t2, VS_HEIGHT
	andi t2, 0xFFFF   ; height &= 0xFFFF;
	sll t2, 0x10      ; height <<= 16;
	li t3, VS_WIDTH
	andi t3, 0xFFFF   ; width &= 0xFFFF;
	addu t2, t3      ; height += width; 
	sw t2, VS_GP0(t0)     ; *vs_gp0 = height;
TransferPaletteToVram:
	li t2, VS_CPU_TO_VRAM  ; vs_cmd_cpu_to_vram = 0xA0000000;
	sw t2, VS_GP0(t0)     ; *vs_gp0 = vs_cmd_cpu_to_vram;
	li t2, 0               ; x = 0;
	li t3, 256             ; y = 256;
	sll t3,  0x10      ; y <<= 16;
	addu t3,  t2      ; y += x;
	sw t3, VS_GP0(t0)     ; *vs_gp0 = y;
	li t2, 256             ; w = 256;
	li t3, 1               ; h = 1;
	sll t3, 0x10      ; h <<= 16;
	addu t3,  t2      ; h += w;
	sw t3, VS_GP0(t0)     ; *vs_gp0 = h;
	li t2, 256             ; y = 256; 
	sll t1, t2, 0x1       ; size <<= 1;
	sra t1, 0x2       ; size /= 4;
	la a0, Palette
TransferPaletteLoop:
	lw t2,0(a0)
	addiu a0, 0x4
	sw t2, VS_GP0(t0)
	bnez t1, TransferPaletteLoop
	subi t1, 0x1
TransferImageToVram:
	li t2, VS_CPU_TO_VRAM  ; vs_cmd_cpu_to_vram = 0xA0000000;
	sw t2, VS_GP0(t0)     ; *vs_gp0 = vs_cmd_cpu_to_vram;
	li t2, 256             ; x = 256;
	li t3, 256             ; y = 256;
	sll t3, 0x10      ; y <<= 16;
	addu t3, t2      ; y += x;
	sw t3, VS_GP0(t0)     ; *vs_gp0 = y;
	li t2, VS_IMGW         ; w = VS_IMGW;
	sra t2, 0x1       ; w /= 2;
	li t3, VS_IMGH         ; h = VS_IMGH;
	sll t3,0x10      ; h <<= 16;
	addu t3, t2      ; h += w;
	sw t3, VS_GP0(t0)     ; *vs_gp0 = h;
	li t1, VS_IMGW         ; x = VS_IMGW;
	li t2, VS_IMGH         ; y = VS_IMGH;
	mult t1, t2           ; size = x * y;
	mflo t1 
	sra t1, 0x2       ; size /= 4;
	la a0, Sprite
TransferLoop:
	lw t2,0(a0)
	addiu a0, 0x4
	sw t2, VS_GP0(t0)
	bnez t1, TransferLoop
	subi t1, 0x1
	addi sp, -72 
TextureSprite:
	li a0, 0x0
	li a1, 0x0
	li a2, 16384
	li a3, 0x0
	sw zero, 16(sp)
	sw zero, 20(sp)
	li t1, VS_HEIGHT
	sw t1, 24(sp)
	li t1, 148 
	sw t1, 28(sp)
	sw zero, 32(sp)
	li t1, VS_IMGH 
	sw t1, 36(sp)
	li t1, VS_WIDTH
	sw t1, 40(sp)
	sw zero, 44(sp)
	li t1, VS_IMGW 
	sw t1, 48(sp)
	sw zero, 52(sp)
	li t1, VS_WIDTH
	sw t1, 56(sp)
	li t1, VS_HEIGHT
	sw t1, 60(sp)
	li t1, VS_IMGW 
	sw t1, 64(sp)
	li t1, VS_IMGH 
	sw t1, 68(sp)
	jal VS_TextureFourPointPoly
	nop
main:
	b main 
	nop
	addi sp, sp, 72

# Function: VS_TextureFourPointPoly
# Purpose: Draws a textured four-point polygon, a quad, to the display area using the GPU 
# a0: x1, a1: y1, a2: palette, a3: u1, 16(sp): v1, 20(sp): x2, 24(sp): y2, 28(sp): texpage, 32(sp): u2, 36(sp): v2, 40(sp): x3, 44(sp): y3, 48(sp): u3, 52(sp): v3
# 56(sp): x4, 60(sp): y4, 64(sp): u4, 68(sp): v4
VS_TextureFourPointPoly:                     
	li   t1, VS_TEXTURE_FOUR_POINT_POLY      ; gpu0_cmd = VS_TEXTURE_FOUR_POINT_POLY;
	sw   t1, VS_GP0(t0)                     ; *vs_gpu0 = gpu0_cmd;
	andi a0, 0xFFFF                     ; x1 &= 0xFFFF;
	sll  a1, 0x10                       ; y1 <<= 16;
	or   a1, a0                        ; y1 |= x1;
	sw   a1, VS_GP0(t0)                     ; *vs_gpu0 = y1;
	sll  a2,0x10                       ; palette <<= 16;
	lhu  a1, 16(sp)
	andi a3, 0xFF                       ; u1 &= 0xFF; 
	andi a1,0xFF                       ; v1 &= 0xFF;
	sll  a1, 0x8                        ; v1 <<= 8;
	or   a1, a3                        ; v1 |= u1;
	or   a1, a2                        ; v1 |= palette;
	sw   a1, VS_GP0(t0)                     ; *vs_gpu0 = v1;
	lhu  a0, 20(sp)
	lhu  a1, 24(sp)
	andi a0, 0xFFFF                     ; x2 &= 0xFFFF;
	sll  a1, 0x10                       ; y2 <<= 16;
	or   a1, a0                        ; y2 |= x2;
	sw   a1, VS_GP0(t0)                     ; *vs_gpu0 = y2;
	lhu  a1, 36(sp)
	lhu  a2, 28(sp)
	lhu  a3, 32(sp)
	sll  a2, 0x10                       ; texpage <<= 16;
	andi a3, 0xFF                       ; u2 &= 0xFF; 
	andi a1, 0xFF                       ; v2 &= 0xFF;
	sll  a1, 0x8                        ; v2 <<= 8;
	or   a1, a3                        ; v2 |= u2;
	or   a1, a2                        ; v2 |= texpage;
	sw   a1, VS_GP0(t0)                     ; *vs_gpu0 = v2;
	lhu  a0, 40(sp)
	lhu  a1, 44(sp)
	andi a0,  0xFFFF                     ; x3 &= 0xFFFF;
	sll  a1,  0x10                       ; y3 <<= 16;
	or   a1, a0                        ; y3 |= x3;
	sw   a1, VS_GP0(t0)                     ; *vs_gpu0 = y3;
	lhu  a3, 48(sp)
	lhu  a1, 52(sp)
	andi a3, 0xFF                       ; u3 &= 0xFF; 
	andi a1,  0xFF                       ; v3 &= 0xFF;
	sll  a1, 0x8                        ; v3 <<= 8;
	or   a1,  a3                        ; v3 |= u3;
	sw   a1, VS_GP0(t0)                     ; *vs_gpu0 = v3;
	lhu  a0, 56(sp)
	lhu  a1, 60(sp)
	andi a0, 0xFFFF                    ; x4 &= 0xFFFF;
	sll  a1,  0x10                      ; y4 <<= 16;
	or   a1,a0                       ; y4 |= x4;
	sw   a1, VS_GP0(t0)                    ; *vs_gpu0 = y4;
	lhu  a2, 64(sp)
	lhu  a3, 68(sp)
	andi a2, 0xFF                      ; u4 &= 0xFF;
	sll  a3, 0x8                       ; v4 <<= 8;
	or   a3, a2                       ; v4 |= u4;
	sw   a3, VS_GP0(t0)                    ; *vs_gpu0 = v4;
	jr ra
	nop

